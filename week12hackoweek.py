# --- COLAB BACKEND CONNECTOR ---
import google.colab.output

def notebook_ingest(hr, steps):
    wearable_data = {"heart_rate": hr, "steps": steps}

    # Process the data
    encrypted_blob = encrypt_and_store(wearable_data)

    print(f"\n[WEBSOCKET] Received Data: HR={hr}, Steps={steps}")
