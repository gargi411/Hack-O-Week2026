# --- ENCRYPTION SETUP ---
# Generate a key for the Fernet symmetric encryption
key = Fernet.generate_key()
cipher_suite = Fernet(key)

app = Flask(__name__)
socketio = SocketIO(app, cors_allowed_origins="*")

# --- BACKEND LOGIC ---
def encrypt_and_store(data):
    # Convert dictionary to JSON string then to bytes
    json_payload = json.dumps(data).encode()

    # REQUIREMENT: Encrypt payloads before database insert
    encrypted_token = cipher_suite.encrypt(json_payload)

    # Simulation: Printing the encrypted 'blob' that would go to your DB
    return encrypted_token

